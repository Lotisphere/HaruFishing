# 4. 文档提炼提示词 (用于模块 A - Extractor)
EXTRACTOR_PROMPT = """你是一个专业的剧本拆解大师。
请阅读以下提供的文档文本，从中提炼出社交圈推演所需的设定信息。

文档内容：
---
{document_text}
---

任务要求：
1. 提炼触发事件 (trigger_event)：从文中找出一个引发后续故事或可能导致冲突的“核心事件”。一句话概括。
2. 提取角色信息 (characters)：找出与该事件最密切相关的核心人物（建议 2-4 人）。
3. 角色刻画 (identity)：为每个角色总结其“性格特点、身份背景、潜在弱点”。
4. 梳理人际关系 (relationships)：分析每个角色与其他角色的具体关系状态和内心看法。

约束：
1. 必须严格以纯 JSON 格式输出，不要输出任何其他废话。
2. 你的 JSON 必须严格遵循以下键名和结构（不要自己发明键名，也不要漏掉任何一个）：
{{
    "trigger_event": "一句话概括的核心事件",
    "characters": [
        {{
            "name": "角色名字",
            "identity": "性格特点、身份背景、潜在弱点的详细描述",
            "relationships": {{
                "另一个角色的名字": "对待他的态度或关系描述"
            }}
        }}
    ]
}}
"""


